<br /><br />
<strong>What are the combs?</strong>
<br /><br />
A room with racked servers, desktop servers, and networking gear where zombies can put into practice the things they're learning in the braindump. The space 'belongs' to the current crop of zombies. 
<br /><br />
<strong>What is this page?</strong>
<br /><br />
This is where combs dwellers claim an IP address for themselves. <br /><br />
It's also a reference when we need to find the owner or purpose of a box. 
<br /><br />
<strong>What's the difference between 'noobz' and 'projects'?</strong>
<br /><br />
1. Go read the entirety of the Chronicle Catacombs entry.
<br /><br />
2. These being two separate VLANs, there are two separate IP ranges. You'll want to start with an IP address in the 'noobz' range if this is your first box. Use 'projects' only if you know you should.
<br /><br />
<strong>Okay, I want a box in the combs. What do?</strong>
<br /><br />
1. Get in touch with your TopDog and make sure it's all right. The previous group should vacate before the new group rolls in. 
<br /><br />
2. Get a combs tour.
<br /><br />
3. Pick an unlabeled physical box in the room. It can be racked or on the floor. If there are no unlabeled boxes, find someone who knows what they're doing to help you identify abandoned boxes.
<br /><br />
<strong>I have the perfect box, now what?</strong>
<br /><br />
1. Find the perfect box name that works with your group's naming scheme.
<br /><br /> 
2. Decide what operating system you want. Windows? Arch? FreeBSD? If you're a Linux noob, give Ubuntu a try. You can always reformat it later and load something else when you've got comfortable.
<br /><br />
3. Decide what services you want to try. Will this box be a webserver? Fileserver? Nameserver? Planning to try your hand at LDAP? Read the Catacombs Chronicle page for suggestions.
<br /><br />
4. What kind of hardware is your box? This is for identifying it later.
<br /><br />
<strong>I've made this list of stuff, but who cares?</strong>
<br /><br />
Your decisions are for the form on the next page. Choose either Noobz VLAN or Projects VLAN from the top of the page. The list of IP addresses are the available ones. Do not change any without checking with their owner first. Addresses owned by 'networking' should not be touched until you know better.
<br /><br />
Choose an IP address from the list that is empty. Fill in the form with your information. 
<br /><br />
<strong>I made a mistake, changed my box name, decided to give up Arch for Windows ... what do I do?</strong>
<br /><br />
The form updates the whole row of the IP address. Put in the address and leave the rest blank to give up your claim to the box, otherwise update all the fields.
<br /><br />
<strong>Now what?</strong>
<br /><br />  
1. IMMEDIATELY go get a sharpie/tape or the labelmaker and physically label your box with its IP address, its name, and your name. This is Good Combs Hygiene. Unlabeled boxes, regardless of Combs DB status, will be reassigned. 
<br /><br />
2. Load your box! Then go back to Chronicle Catacombs page and continue with 'Setting up DNS on Boxes'.
<br /><br />
<strong>Your site broke. I tried to name my box after Little Bobby Tables and it died.</strong>
<br /><br />
Ping wicker or email wicker@cat.pdx.edu if there's a technical problem.
<br /><br />
<strong>Someone stole my box. I don't know what to do.</strong>
<br /><br />
Talk to your TopDog.
<br /><br />
<strong>I'm scared.</strong>
<br /><br />
Don't be. The combs are for sneaking about in the dead of night with an O'Reilly book in one hand.  Think of it like this: if you're too scared to break something in the combs, where things are meant to be broken, do you think anyone will ever let you into the datacenter? Break things. Make mistakes. That's how you learn. 
